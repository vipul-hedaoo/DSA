    { int totalgas=0,expencegas=0,fule=0,index=a.length;
        for(int i=0;i<a.length;i++)
        {
            totalgas+=a[i];
            expencegas+=b[i];
        }
        if(totalgas-expencegas>=0)
        {
           for(int j=0;j<a.length;j++)
           {
               fule+=a[j];
               if(fule>=b[j])
               {
                   fule-=b[j];
                   index=Math.min(index,j);
               }
               else{
                   fule=0;
                   index=j+1;
               }
           }
            return index;
        }
        return -1;

    }


